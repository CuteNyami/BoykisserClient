package net.luconia.boykisser.module;

/**
 * Module size rect
 * <br>
 * Used for visual modules
 * @author Nyami
 */
public class ModuleRect {
    private final float x;
    private final float y;

    public ModuleRect(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
